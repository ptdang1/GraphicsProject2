package cs304.graphics2;


public class Vec2D
{
   double x;
   double y;
   
   public double getX()
   {
      return x;
   }
   public double getY()
   {
      return y;
   }
   public void setX(double x)
   {
      this.x = x;
   }
   public void setY(double y)
   {
      this.y = y;
   }
}